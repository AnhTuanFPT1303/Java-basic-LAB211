    public boolean isEmptyInput() {
        if (jTextField1.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "ID is missing");
            return false;
        }
        if (jTextField2.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Name is missing");
            return false;
        }
        if (jTextField3.getText().matches("^.@.+\\..+$")) {
            JOptionPane.showMessageDialog(this, "Invalid email input");
            return false;
        }
        if (jTextField4.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Phone number is missing");
            return false;
        }
        if (jTextField5.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Enter your Father's name");
            return false;
        }
        if (jTextField6.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Enter your Mother's name");
            return false;
        }
        if (jTextField7.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Enter your address");
            return false;
        }
        
        if (jDateChooser1.getDate() == null) {
            JOptionPane.showMessageDialog(this, "Enter your Birthday");
            return false;
        }
        
        if (jDateChooser1.getDate().compareTo(new Date())>0) {
            JOptionPane.showMessageDialog(this, "This student come from future ???");
            return false;
        }
        
        
        return true;
    }
    
    
