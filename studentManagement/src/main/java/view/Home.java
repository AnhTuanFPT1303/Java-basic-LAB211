    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        tModel = (DefaultTableModel) jTable1.getModel();
        int rowIndex = jTable1.getSelectedRow();
        jTextField1.setText(tModel.getValueAt(rowIndex, 0).toString());
        jTextField2.setText(tModel.getValueAt(rowIndex, 1).toString());
        try {
            Date date = new SimpleDateFormat("dd/mm/yyyy").parse(tModel.getValueAt(rowIndex, 2).toString());
            jDateChooser1.setDate(date);
        } catch (ParseException p) {}
        String gender = tModel.getValueAt(rowIndex, 3).toString();
        if (gender.equals("Male")) 
            jComboBox1.setSelectedIndex(0);
        else jComboBox1.setSelectedIndex(1);
        jTextField3.setText(tModel.getValueAt(rowIndex, 4).toString());
        jTextField4.setText(tModel.getValueAt(rowIndex, 5).toString());
        jTextField5.setText(tModel.getValueAt(rowIndex, 6).toString());
        jTextField6.setText(tModel.getValueAt(rowIndex, 7).toString());
        jTextField7.setText(tModel.getValueAt(rowIndex, 8).toString());
    }//GEN-LAST:event_jTable1MouseClicked

