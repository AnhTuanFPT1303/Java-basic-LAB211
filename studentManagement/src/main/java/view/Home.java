    @Override
    public void searchMachine() {
        String searchInput = jTextField8.getText();
        if (!searchInput.isEmpty()) {
            tModel.setRowCount(0);
            char[] words = searchInput.toCharArray();
            boolean isString = true;
            for (int i=0; i<words.length; i++) {
                if(Character.isDigit(words[i]) == true)
                   isString = false ;
            }
                if (isString == false) {
                for (Student s: studentList.getStudentList()) {
                    if (s.getId().equals(searchInput)) {
                        Object[] newRow1 = {s.getId(), s.getName(), s.getDateOfBirth(), s.getGender(), s.getEmail(), s.getPhoneNumber(), s.getFatherName(), s.getMotherName(), s.getAddress()};
                        tModel.addRow(newRow1);
                    }                
                }
            }
            else {
              for (Student s: studentList.getStudentList()) {
                  if (s.getName().toLowerCase().contains(searchInput.toLowerCase())) {
                      Object[] newRow2 = {s.getId(), s.getName(), s.getDateOfBirth(), s.getGender(), s.getEmail(), s.getPhoneNumber(), s.getFatherName(), s.getMotherName(), s.getAddress()};
                      tModel.addRow(newRow2);
                  }
              }  
            }
        }
    }

    @Override
    public void deleteStudent() {
        int selectedRow = jTable1.getSelectedRow();
        if (selectedRow != -1) {
            tModel.removeRow(selectedRow);
            studentList.studentRemove(studentList.getStudentList().get(selectedRow));
            studentList.studentWrite();
            JOptionPane.showMessageDialog(null, "Student deleted sucessfully");
        }
    }
    
    @Override
    public void studentGetValue() {
        tModel.setRowCount(0);
        for (Student student: studentList.getStudentList()) {
            Object[] row = {student.getId(), student.getName(), student.getDateOfBirth(), student.getGender(), student.getEmail(), student.getPhoneNumber(), student.getFatherName(), student.getMotherName(), student.getAddress()};
            tModel.addRow(row);
        }
    }

    @Override
    public void studentAdd() {
        boolean flag = true;
        for (Student s: studentList.getStudentList()) {
                if (s.getId().equals(jTextField1.getText())) {
                    JOptionPane.showMessageDialog(null, "Duplicate ID, please enter another id");            
                    flag = false;
                }
        }
        if (isEmptyInput() && flag) {
            String id = jTextField1.getText();
            String name = jTextField2.getText();
            SimpleDateFormat simpleDateFormat = new SimpleDateFormat("dd/mm/yyyy");
            String dateOfBirth = simpleDateFormat.format(jDateChooser1.getDate());
            String gender = jComboBox1.getSelectedItem().toString();
            String email = jTextField3.getText();
            String phoneNumber = jTextField4.getText();
            String fatherName = jTextField5.getText();
            String motherName = jTextField6.getText();
            String address = jTextField7.getText();
            Student student = new Student(id, name, dateOfBirth, gender, email, phoneNumber, fatherName, motherName, address);
            studentList.addStudentList(student);
            Object[] newRow = {id, name, dateOfBirth, gender, email, phoneNumber, fatherName, motherName, address};
            tModel.addRow(newRow);
            JOptionPane.showMessageDialog(null, "Student added successfully");
            studentList.studentWrite();
            clearStudent();
         }
    }
    @Override
    public void studentUpdate() {
        if (isEmptyInput()) {
            String id = jTextField1.getText();
            String name = jTextField2.getText();
            SimpleDateFormat dateFormat = new SimpleDateFormat("dd/mm/yyyy");
            String date = dateFormat.format(jDateChooser1.getDate());
            String gender = jComboBox1.getSelectedItem().toString();
            String email = jTextField3.getText();
            String phone = jTextField4.getText();
            String fatherName = jTextField5.getText();
            String motherName = jTextField6.getText();
            String address = jTextField7.getText();
            Student s = new Student(id, name, date, gender, email, phone, fatherName, motherName, address);
            studentList.studentUpdate(s);
            studentList.studentWrite();
            JOptionPane.showMessageDialog(null, "Student updated successfully");
        }
    }
